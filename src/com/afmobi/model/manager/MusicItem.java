package com.afmobi.model.manager;

public class MusicItem {
	
	private String itemID="";
	private String publicYear="";
	private String singer="";
	private String publicCountry="";
	private String lan="";
	public String getItemID() {
		return itemID;
	}
	public void setItemID(String itemID) {
		this.itemID = itemID;
	}
	public String getPublicYear() {
		return publicYear;
	}
	public void setPublicYear(String publicYear) {
		this.publicYear = publicYear;
	}
	public String getSinger() {
		return singer;
	}
	public void setSinger(String singer) {
		this.singer = singer;
	}
	public String getPublicCountry() {
		return publicCountry;
	}
	public void setPublicCountry(String publicCountry) {
		this.publicCountry = publicCountry;
	}
	public String getLan() {
		return lan;
	}
	public void setLan(String lan) {
		this.lan = lan;
	}
	
	
}
